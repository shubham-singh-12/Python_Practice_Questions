# Write a Python program to swap two variables.

print("\n========== Write a Python program to swap two variables. ==========\n")

def swapNumbers():
  number_1 = int(input("Enter 1st number: "))
  number_2 = int(input("Enter 2nd number: "))
  
  number_1, number_2 == number_2, number_1

  print(f"\n Before Swapping: \n 1st number is: {number_1} \n 2nd number is: {number_2}")
  print(f"\n After Swapping: \n 1st number is: {number_2} \n 2nd number is: {number_1}")

swapNumbers()